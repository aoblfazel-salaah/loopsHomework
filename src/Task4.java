public class Task4 {
    public static void main(String[] args) {
        int n = 20;

        while (n > 0) {
            if (n % 2 == 0) {
                System.out.println(n);
            }
            n--;
        }
        int n1 = 1;

        while (n1 <= 20) {
            if (n1 % 2 == 0) {
                System.out.println(n1);
            }
            n1++;
        }
    }
}
